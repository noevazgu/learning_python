# The == operator compares the values of two operands, if they are equal the result of the comparasion is True,
# if they are not equal the result is False.

pc_number = 4
user_number = int(input("your number:"))
print(pc_number == user_number)

# We can also compare if the operands are NOT EQUAL with !=
pepsi = 21
fanta = 20
print(pepsi != fanta) # True because the values are NOT EQUAL

