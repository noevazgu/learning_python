# concatenation
# + operactor can be use with strings to join (concatenate) strings
name = "noe"
last_name = "vazquez"
full_name = name + " " + last_name
print(full_name)
# if you wan to concatenate a number with a string you must first cast that number into a string:
sentence = "I am " + str(24) + " years old"
print(sentence)

# replicate strings
# the asterisk can be use to repeat one string X times:
dog_sound = "woof" * 5
print(dog_sound)
